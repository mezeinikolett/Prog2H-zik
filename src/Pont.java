public class Pont {
    private double pont1;
    private double pont2;

    public Pont(double pont1, double pont2) {
        this.pont1 = pont1;
        this.pont2 = pont2;
    }

    public double getPont1() {
        return pont1;
    }

    public void setPont1(double pont1) {
        this.pont1 = pont1;
    }

    public double getPont2() {
        return pont2;
    }

    public void setPont2(double pont2) {
        this.pont2 = pont2;
    }
    
    
            
    
}
